function M=oneBitsM(p1, p2);

M = [[p1(1) * p2(1), p1(1) * (1 - p2(1)), p2(1) * (1 - p1(1)), (1 - p1(1)) * (1 - p2(1))];
     [p2(1) * p1(2), p1(2) * (1 - p2(1)), p2(1) * (1 - p1(2)), (1 - p2(1)) * (1 - p1(2))];
     [p1(1) * p2(2), p1(1) * (1 - p2(2)), p2(2) * (1 - p1(1)), (1 - p1(1)) * (1 - p2(2))];
     [p1(2) * p2(2), p1(2) * (1 - p2(2)), p2(2) * (1 - p1(2)), (1 - p1(2)) * (1 - p2(2))];];
end